def connect_callback(widg):
    widg.transfer_button.pressed.connect(widg.toggle_transfer_data)
    widg.csv_button.pressed.connect(widg.csv_data)
    widg.Plot_csv.pressed.connect(widg.plot_csv)
    widg.spice_button.pressed.connect(widg.ltspice_data)
    widg.Plot_spice.pressed.connect(widg.plot_ltspice)
    widg.delete_button.pressed.connect(widg.clear)
    widg.bode_button.pressed.connect(widg.bode)
    widg.plotOptionsButton.pressed.connect(widg.toolbar.edit_parameters)
    widg.figOptionsButton.pressed.connect(widg.toolbar.configure_subplots)
    widg.saveButton.pressed.connect(widg.toolbar.save_figure)
    widg.zoom_button.toggled.connect(widg.zoom)
    widg.resetButton.pressed.connect(widg.reset)
    widg.exit_button.pressed.connect(exit)
    widg.new_plot_button.pressed.connect(widg.newPlot)
    widg.button_active_resp.pressed.connect(widg.toggle_signal_resp)
    widg.signal_type_box.currentIndexChanged.connect(widg.toggle_freq_duty)
    widg.button_signal_response.pressed.connect(widg.plot_response)
    widg.pan_button.toggled.connect(widg.pan)